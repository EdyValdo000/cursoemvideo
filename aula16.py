# Tuplas são variaveis compostas que se criam com parenteses.
# As tuplas são imutáveis
lanche = ('Hamburger', 'Suco', 'Pudin', 'Pizza')
for pos, comer in enumerate(lanche):
    print(f'Eu vou comer {comer} na posição {pos}')

print(lanche)
print(sorted(lanche))

pessoa = ('Edivaldo', 21, 'M', 54.25)
print(pessoa)
